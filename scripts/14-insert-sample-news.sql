WITH article_tag_mapping AS (
    SELECT 
        na.id as article_id,
        nt.id as tag_id
    FROM news_articles na
    CROSS JOIN news_tags nt
    WHERE 
        (na.title LIKE '%비자%' AND nt.name IN ('속보', '정치', '한국', '태국', '비자')) OR
        (na.title LIKE '%지하철%' AND nt.name IN ('사회', '방콕', '교통')) OR
        (na.title LIKE '%바트화%' AND nt.name IN ('경제', '태국', '한국', '투자')) OR
        (na.title LIKE '%추석%' AND nt.name IN ('교민', '방콕', '한국', '행사')) OR
        (na.title LIKE '%K-POP%' AND nt.name IN ('문화', '방콕', '한국')) OR
        (na.title LIKE '%축구%' AND nt.name IN ('스포츠', '방콕', '한국')) OR
        (na.title LIKE '%디지털 노마드%' AND nt.name IN ('IT', '태국', '한국', '비자')) OR
        (na.title LIKE '%한국마트%' AND nt.name IN ('생활', '방콕', '교민', '한국')) OR
        (na.title LIKE '%정상회담%' AND nt.name IN ('정치', '한국', '태국', '국제')) OR
        (na.title LIKE '%우기철%' AND nt.name IN ('안전', '태국', '교민', '날씨'))
)
INSERT INTO news_article_tags (article_id, tag_id)
SELECT DISTINCT article_id, tag_id
FROM article_tag_mapping
ON CONFLICT (article_id, tag_id) DO NOTHING;
